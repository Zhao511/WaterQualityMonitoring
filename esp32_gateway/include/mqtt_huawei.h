/**
 * ============================================================
 * 华为云 IoT MQTT 客户端
 * ============================================================
 * 职责:
 *  - WiFi 连接
 *  - MQTT 设备认证 (设备ID + 密钥)
 *  - 属性上报 (4 服务)
 *  - 命令接收 + 转发到 STM32
 * ============================================================
 */

#ifndef MQTT_HUAWEI_H
#define MQTT_HUAWEI_H

#include <Arduino.h>
#include <WiFi.h>
#include <PubSubClient.h>
#include "config.h"

/* ================================================================
 * 初始化
 * ================================================================ */
bool mqtt_init();
void mqtt_loop();

/* ================================================================
 * 状态查询
 * ================================================================ */
bool mqtt_connected();

/* ================================================================
 * 属性上报 — 直接发到华为云属性 Topic
 * @param  service_json  { "service_id":"...", "properties":{...} }
 * ================================================================ */
void mqtt_report_property(const String &service_json);

/* ================================================================
 * 命令响应 — 回复云端
 * ================================================================ */
void mqtt_response_cmd(const String &rsp_json);

/* ================================================================
 * 命令回调 — 云端下发命令时触发
 * @param  cmd_json  完整命令 JSON
 * ================================================================ */
typedef void (*mqtt_cmd_callback_t)(const String &cmd_json);
void mqtt_on_command(mqtt_cmd_callback_t cb);

#endif /* MQTT_HUAWEI_H */
