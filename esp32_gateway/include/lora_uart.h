/**
 * ============================================================
 * LoRa UART 通信层 — 与 STM32 数据收发
 * ============================================================
 * 数据帧格式: 纯 JSON 文本 (无帧头帧尾，靠 JSON 完整性判断)
 * STM32 → ESP32: 属性上报 / 命令响应
 * ESP32 → STM32: 云端下发命令
 * ============================================================
 */

#ifndef LORA_UART_H
#define LORA_UART_H

#include <Arduino.h>
#include "config.h"

/* ================================================================
 * 初始化 / 轮询
 * ================================================================ */
void lora_init();
void lora_loop();

/* ================================================================
 * 发送 — ESP32 → STM32
 * ================================================================ */
void lora_send(const String &json);

/* ================================================================
 * 接收 — STM32 → ESP32 (回调注册)
 * ================================================================ */
typedef void (*lora_rx_callback_t)(const String &svc, const String &json);
void lora_on_receive(lora_rx_callback_t cb);

/* ================================================================
 * 查询接收缓冲区是否有完整 JSON 帧
 * ================================================================ */
bool lora_available();

#endif /* LORA_UART_H */
